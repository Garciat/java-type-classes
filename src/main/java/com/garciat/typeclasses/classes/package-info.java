/**
 * Standard type class definitions.
 *
 * <p>This package contains interfaces annotated with {@code @TypeClass} that define standard type
 * classes such as:
 *
 * <ul>
 *   <li>{@code Show} - Convert values to strings
 *   <li>{@code Eq} - Equality testing
 *   <li>{@code Ord} - Ordering comparisons
 *   <li>{@code Monoid} - Associative binary operations with identity
 *   <li>{@code Functor} - Mappable type constructors
 *   <li>{@code Applicative} - Application of functions in a context
 *   <li>{@code Monad} - Sequential composition of computations
 *   <li>{@code Foldable} - Structures that can be folded
 *   <li>{@code Traversable} - Structures that can be traversed
 * </ul>
 *
 * <p><b>PUBLIC API</b>: All type classes in this package are part of the public API.
 */
